package mxp.net;

import mxp.packet.Packet;

import java.net.SocketAddress;

/**
 * Interface for classes that handle receiving packets.
 */
public interface PacketHandler {
    /**
     * Handle the event when a new packet just arrived.
     * This function should just grab & store this packet, and return
     * quickly afterwards. All heavy processing should be done outside
     * this function call.
     *
     * @param packet the new packet just received.
     * @param client the address of the client the packet was received from.
     */
    void packetReceived(Packet packet, SocketAddress client);
}
